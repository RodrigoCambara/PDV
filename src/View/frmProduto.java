    private void cbFornecedorAncestorAdded(javax.swing.event.AncestorEvent evt) {//GEN-FIRST:event_cbFornecedorAncestorAdded

        //Carregar combobox fornecedores
        FornecedoresDAO dao = new FornecedoresDAO();

        List<fornecedorClass> listadefornecedores = dao.listarFornecedores();

        cbFornecedor.removeAll();

        for (fornecedorClass f : listadefornecedores) {

            cbFornecedor.addItem(f);

        }

    }//GEN-LAST:event_cbFornecedorAncestorAdded

    private void txtPesquisaKeyPressed(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_txtPesquisaKeyPressed
        // TODO add your handling code here:
        String nome = "%" + txtPesquisa.getText() + "%";

        ProdutosDAO dao = new ProdutosDAO();
        List<produtosClass> lista = dao.buscaProdutosPorNome(nome);
        DefaultTableModel dados = (DefaultTableModel) tabelaProdutos.getModel();
        dados.setNumRows(0);

        for (produtosClass c : lista) {
            dados.addRow(new Object[]{
                c.getId(),
                c.getDescricao(),
                c.getPreco(),
                c.getQtd_estoque(),
                c.getFornecedor().getNome(),});

        }
    }//GEN-LAST:event_txtPesquisaKeyPressed

